package findingsolutions.java;

// this is meant to be a superclass - generic representation of all animals
public class Animal {
	
	/* constructor - all classes in Java have a default method/function
	 * no need to declare explicitly
	 * a constructor function can't have a return type*/
	public Animal() {
		
	}

	public void noiseTheAnimalMakes() {
		System.out.println("The animal makes a high-pitched noise");
	}

}
