package findingsolutions.java;

/* Java escape characters are preceded by backslash(\)*/
public class EscapeCharactersPractice {
	
	public static void main(String[] args) {
		
		System.out.println("Hello world");  // this is plain unchanged text
		
		System.out.println("Hello \t world"); // inserts a tab within the text
		
		System.out.println("This is a \"blue\" pen"); // inserts a double quote within the text/String
	
		System.out.println("option1 \\ option2"); // inserts a backslash within the text
		
		System.out.print("Hello \n world"); // inserts a new line within text
	}

}
